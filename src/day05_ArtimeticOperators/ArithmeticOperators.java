package day05_ArtimeticOperators;

public class ArithmeticOperators {
    public static void main(String[] args) {

        System.out.println(20 + 3 - 2 + 1 );
        //23- 2 +1
        //21+1
        //22
        System.out.println(20 * 3);
        System.out.println( 2+ 3 - 1 * 4 );
                     //23+3
                    // 5-4
                   //1
        System.out.println(10/3); //int/int ==> int
        System.out.println(10.0/3); //double/int= double
        System.out.println(10/3.0); // still double
        System.out.println(10.0/3.0); // still double

        System.out.println(5+2*4);
        System.out.println(10/2-3);
        System.out.println(8 + 12 * 2 -4);
        System.out.println(4+17%2-1);
        System.out.println(6-3*2+7-1);


        System.out.println(10%4);
             // 10/4=2
             // 10-(4*2) = 2
        System.out.println(30%8);
            //   30/8 = 3
            // reminder: 30-(8*3)
        //      30-24=6
        System.out.println(99 % 4);
           // 99/4 = 24
           //   99- (4*24)
           //    99-96 = 3

    }
}
